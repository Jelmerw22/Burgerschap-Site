            <h1>Politiek-juridisch.</h1>
            <p>
                De VVI wordt een directe democratie, want wij staan ervoor dat het volk een mening mag hebben en die ook moet kunnen uitoefenen, het is immers ook hun land. Om dit te realiseren zullen wij in iedere gemeente minstens 1 verzamel punt plaatsen, waar iedereen hun ideeën achter kan laten, ook zullen we door het hele land mobiele verzamel locaties plaatsen, die rondrijden en de meningen verzamelen van de mensen die niet in staat zijn om naar een verzamel punt te reizen. Wij hopen met deze ingrepen dat er meer gelijkheid komt onder de mensen en dat het welzijn van het volk beter wordt. We zullen ervoor zorgen dat iedereen zich gehoord voelt en dat iedereen voelt alsof ze een steentje bijdragen aan hoe het land bestuurd wordt, er zullen dus ook nooit beslissingen worden genomen als het volk er niet mee eens is.   
                    <br>
                    <br>
                De VVI zal zich in het midden van het politiek spectrum plaatsen, want wij staan ervoor om iedereen te helpen die voelt alsof hij of zij niet gehoord worden, maar ook willen wij het land zelfstandiger maken en alleen ingrijpen bij grote incidenten. Gemeentes zullen meer verantwoordelijkheden krijgen voor hun inwoners en zij zullen verantwoordelijk zijn voor hen inwoners. Door dit zo te doen hopen wij meer tijd te krijgen om te luisteren naar wat ieder individu te zeggen heeft. De VVI zal een progressieve partij zijn maar ook een conservatieve partij, want voor ons staat de welvaart van Nederland en het welzijn van het Nederlandse volk op 1, de conservatie aspecten van onze partij licht vooral bij het cultuurbehoud we willen onze Nederlandse tradities niet verliezen, maar met moderniseren hebben wij geen probleem. Wij willen het volk zo tevreden mogelijk houden, maar tegelijk ook veel progressie boeken om Nederland steeds een beter land te maken. 
                    <br>
                    <br>
                De VVI zal een liberale partij zijn, omdat wij de vrijheid van het volk op nummer 1 zetten en ervoor en ervoor willen zorgen dat iedereen zich gehoord en goed voelt in ons land, niemand zou moeten hoeven twijfelen over of ze wel of niet echte vrijheid hebben. Liberalisme staat voor de vrijheid van het individu op nummer 1 zetten. Het doel van de overheid is om het individu de ruimte te geven om zicht te ontplooien. 
                    <br>
                    <br>    
                De VVI zal nauw samenwerken met de EU, we willen graag onze welvaart te verbeteren door meer te handelen met de EU en ook andere regelingen vaststellen met de EU die ervoor kunnen zorgen dat de handel nog makkelijker kan gaan. De VVI hoopt mee te kunnen dragen aan het uitbreiden van de EU en hoopt dat daardoor de welvaart en het welzijn van het volk nog beter wordt. 
                    <br>
                    <br>
                De VVI zal staan voor gelijkheid en vrijheid onder het volk, de VVI vindt erg belangrijk voor een betere samenleving, we willen ervoor zorgen dat iedereen zicht thuis voelt in ons mooie land. Dit zal ongetwijfeld veel moeite kosten, maar de VVI zal er alles aan doen om dit een waarheid te maken, onderscheid op basis van huidskleur, afkomst, ziekte en of handicap past niet meer in onze samenleving en de VVI zal ook streng optreden tegen iedereen die een mede mens anders behandeld vanwege eerder opgenoemde eigenschappen. Ook zal er nog strenger op worden ge treed om discriminatie en racisme te bestrijden. Ook zullen onderdrukken en beledigende aspecten van onze samenleving veranderd zoals bijvoorbeeld de oud Nederlandse traditie sinterklaas, in de samenleving van vandaag past ‘zwarte’ piet niet meer, daardoor zal de VVI ‘zwarte’ piet veranderen naar een meer realistische ‘roetveeg’ piet zodat iedere bevolkings groep zich er goed bij kan voelen. 
