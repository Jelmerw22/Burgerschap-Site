            <h1>Sociaal-cultureel</h1>
            <p>
            De VVI heeft een gemixte visie op het vluchtelingenprobleem, we vinden dat het verminderd moet worden maar we willen ook nog onze hulp aanbieden aan de mensen in nood. We willen voorkomen dat het land overspoeld en we willen daarom niet meer mensen in nemen als dat wij kunnen, als we vol zitten, zitten wel vol. 
                <br>
            We zullen op verschillende plekken in t land opvang locatie maken waar ze kunnen onderblijven totdat ze kunnen worden geïntegreerd in de samenleving, we willen er alles aan doen om ervoor te zorgen dat de nieuw geïntrigeerde geen overlast veroorzaken. 
                <br>
            Ook willen we graag dat ze zo snel mogelijk kunnen beginnen met werken en hun eigen plek kunnen krijgen.
            </p>
            <p>
            Bij de VVI is de visie op de pluriforme samenleving dat men meer moet integreren, cultuurbehoud vinden wij erg belangrijk en wij vinden dus niet dat degene die zicht invoegt in onze cultuur zijn eigen cultuur moet vergeten en moet achterlaten. 
                <br>
            Wij vinden het wel erg belangrijk dat iedereen de basis van de Nederlandse cultuur volgt zodat we een zo een mogelijk land zijn, en zo dat de verschillen onder de verschillende bevolkingsgroepen zo klein mogelijk zijn en blijven.  
                <br>
            Hoewel we het belangrijk vinden dat iedereen in de basis hetzelfde volgt verplichten we niemand om iets te volgen en of hun eigen geloof en cultuur achterlaten, iedereen is vrij om te doen wat ze willen en om te geloven en te volgen wat ze willen. We hopen wel een zo hecht mogelijke samenleving te maken. 
            </p>
            <p>
            In de samenleving zal altijd een vorm van discriminatie zijn of het op basis van af komst, huidskleur, ziekte of lichamelijke beperking is doet er niet toe, de VVI is zwaar tegen elke vorm van discriminatie. De VVI zal hierom ook alles eraan doen om dit uit de samenleving te krijgen, hoewel dit erg lastig kan zijn want, in sommige gevallen is het van kinds af aan aangeleerd en hebben de kinderen soms helemaal geen idee dat wat zij doen discriminerend is. 
                <br>
            Om dit te ver komen willen wij de opvoeding van jonge kinderen goed in de gaten houden door middel van docenten en andere invloed rijken mensen in hun jeugd, ook zullen deze invloedrijke mensen duidelijk maken wat discriminatie is en wat het inhoudt. Hiermee hopen wij de discriminatie in onze samenleving te verminderen. 
            </p>
            <p>
            Voor de VVI staan bepaalde normen en waardes centraal en zijn deze erg belangrijk en hopen wij dat deze gedeeld worden door het volk en dat zij ze navolgen.  
                <br>
            Wij vinden dat racisme in deze tijd echt niet meer kan, wij vinden dat iedereen gelijk is en tegelijk speciaal is in ieder zijn eigen manier. Mensen mogen niet beoordeeld en veroordeeld worden op basis van hun huidskleur en of afkomst. Is dit wel het geval? Dan zullen er in ons rechtssysteem zware gevolgen zijn. 
                <br>
            Seksisme is ook iets dat niet meer kan, mensen op basis van hun geslacht anders behandelen is iets waar de VVI zwaar tegen is. 
                <br>
            Ook vinden wij bij de VVI het erg belangrijk dat iedereen de cultuur van Nederland respecteert, wij vinden niet dat iedereen de cultuur van Nederland moet volgen, maar wel respecteren. Ook vinden wij dat Nederlandse tradities gerespecteerd moeten worden, maar ook dat de tradities zo aangepast moet worden dat het voor iedereen is en iedereen het leuk kan vinden. 
                <br>
            De VVI vindt het normaal en goed behandelen van de mede mens ook erg belangrijk, help waar je kan helpen. Als je bijvoorbeeld de mogelijkheid hebt om je buren ergens mee te helpen, doe het. Als we dit allemaal vaker doen dan kunnen we zorgen voor een beter band onder onze bevolking en het zal ook veel goeds doen voor ons welzijn. 
            </p>
