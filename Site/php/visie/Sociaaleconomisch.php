            <h1>Sociaaleconomisch.</h1>
            <p>
                De VVI gaat een nachtwakerstaat worden, want wij vinden dat de overheid zo min mogelijk moet bemoeien met de burgers. Wij vinden dat de overheid als enige taken ervoor moet zorgen dat de inwoners ten alle tijden veilig zijn en dat ze moeten zorgen dat er altijd genoeg politie en genoeg krijgsmacht is. Daarnaast vinden wij dat er ook een aantal strenge wetten moeten zijn om de rechtsorde te handhaven, ook zal er streng opgeteerd worden naar degene die deze wetten overtreden. 
                    <br>
                Een nachtwakerstaat is in onze ogen de beste manier om ervoor te zorgen dat iedereen zich vrij voelt en het zorgt voor meer zelfstandigheid onder de bevolking, inwoners zullen niet meer zoveel op de overheid kunnen leunen om hun problemen op te lossen. Ook zal een nachtwakerstaat ervoor zorgen dat de overheid meer tijd over heeft voor andere zaken. 
            </p>
            <p>
                Wij vinden dat de overheid er altijd alles aan moet doen om ervoor te zorgen da t het welzijn van het volk goed is, dit kan door in geïsoleerde gebieden nieuwe projecten te starten zoals de bouw van een gemeentehuis of een buurt huis. 
                    <br>
                Ook moet de overheid goed in de gate houden dat de politie goed te werk gaat en dat de politie de juiste gereedschappen hebben om hun werk juist te verrichten. In gebieden waar het welzijn laag is wordt goed geluisterd naar wat het volk wil zodat ze zich weer beter kunnen voelen en zodat hun omstandigheden beter kunnen worden. 
            </p>
            <p>
                De voorzieningen die wij belangrijk vinden zijn de volgende voorzieningen; zorgtoeslag, studiefinanciering, werkeloosheidsuitkering, ziektewetuitkering, zwangerschapsuitkering en een adaptie/pleegzorg uitkering. 
                    <br>
                Wij vinden een zorgtoeslag belangrijk omdat wij het erg belangrijk vinden dat iedereen toegang heeft tot de juiste en de beste zorg zodat ons volk gezond en wel blijft. 
                    <br>
                Een studiefinanciering vinden wij belangrijk omdat de studenten die hier recht op hebben onze toekomst zijn en in veel gevallen kan een studenten leven erg druk zijn, dus een beetje hulp kunnen zij goed gebruiken, de hoeveelheid studiefinanciering licht aan de thuissituatie en is dus voor iedere student anders.  
                    <br>
                De werkeloosheidsuitkering krijg je recht op, op het moment dat je een bepaalde periode zonder werk zit en je actief op zoek bent naar nieuw werk en dus ALLEEN als je actief nieuw werk zoekt en dit kan bewijzen. Want vinden deze uitkering belangrijk omdat wij snap dat het stress vol kan zijn om een nieuwe baan te vinden nadat je voor welke reden dan ook je vorige baan bent verloren, en om deze stress te verminderen is er een vergoeding. 
                    <br>
                Een ziektewetuitkering krijgt als je langdurig ziek bent om ervoor te zorgen dat de situatie niet erger wordt, voor deze uitkering is bewijs wel noodzakelijk en het bijhouden van bewijs ook, als hierin gefaald wordt kan de uitkering teruggetrokken worden. 
                    <br>
                Zwangerschapsuitkering is beschikbaar voor mensen die zwanger zijn en hierdoor niet kunnen werken. 
                    <br>
                De adaptie/pleegzorg uitkering is beschikbaar voor mensen die een adoptie of pleegkind in hun gezin hebben, de hoeveelheid van de uitkering licht aan de gezinssituatie en is dus voor iedereen anders. 
            </p>
            <p>
                Sociale ongelijkheid vinden wij de ongelijke verdeling van personen en groepen in belangrijke zaken en de ongelijke behandeling en waardering van hen op basis van maatschappelijke positie een leefstijl. Ook de rechten en plichten zijn niet gelijk verdeeld. 
                    <br>
                Wij vinden dat de sociale ongelijkheid verminderd moet worden. Dit willen wij doen door het eerst te herkennen, waar vinden wij de sociale ongelijkheid? De sociale ongelijkheid kan verbeterd worden door meer verbinding te creëren onder verschillende bevolkingsgroepen. 
                    <br>
                Maar er is ook duidelijk te zien dat de sociale ongelijkheid in ons land niet zo groot is en dus gelukkig niet zo een groot probleem vormt, dus in onze ogen is de sociale ongelijkheid oplossen niet de hoogste prioriteit. 
                    <br>
                Wel vinden we het belangrijk dat de culturele en cognitieve ongelijkheid verminderd wordt wij vinden het namelijk erg belangrijk dat iedereen toegang moet hebben tot de dezelfde kennis en dat niemand achtergesteld mag worden, ook vinden we dat de economische ongelijkheid verminderd moet worden, dit willen wij doen door middel van meer steun aan de mensen onderaan het spectrum. 
            </p>
